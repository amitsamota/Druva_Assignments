c = 'Python'
# break example
for i in c:
    if i == 'h':
        break
    else:
        print(i)

#continue example
for i in c:
    if i == 'h':
        continue
        print('this is continue')
    else:
        print(i)

#pass example    pass is null used when there is nothing to do
for i in c:
    if i == 'h':
        pass
        print('this is pass')
    else:
        print(i)




